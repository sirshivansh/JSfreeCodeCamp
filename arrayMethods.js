// Arrays have their own methods!
let arr = ["Rick","Nick","Pick"];
console.log(arr); // arr is displayed

let pushed = arr.push("Zig"); // .push() method
console.log(pushed); // .push() returns new length of the array (neumerical value)
console.log(arr); // updated array is displayed

let popped = arr.pop(); // .pop() method
console.log(popped); // popped element is displayed
console.log(arr); // updated array is displayed